#include<bits/stdc++.h>
using namespace std;

int main()
{
	double s,v,m;
	int n,a,t,b;
	cin>>s>>v;
	n=8*60+24*60;//两天总共的分钟数
	t=ceil(s/v)+10;
	n=n-t;//得出剩下的时间。
	if(n>=24*60) n-=24*60;//判断是否在前一天。
	b=n%60;//分。
	a=n/60;//时
	if(a<10)//判断是否补0
	{
		if(b<10) cout<<"0"<<a<<":0"<<b;
		else cout<<"0"<<a<<":"<<b;
	}
	else
	{
		if(b<10) cout<<a<<":0"<<b;
		else cout<<a<<":"<<b;
	}
	return 0;
}
